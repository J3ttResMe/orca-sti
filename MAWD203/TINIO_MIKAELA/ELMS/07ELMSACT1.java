public class Cat {
    private String catType;
    private int cutenessLvl;

    public Cat() {
        this.catType = "Unknown";
        this.cutenessLvl = 3;
    }
    public Cat(String type, int lvl) {
        this.catType = type;
        this.cutenessLvl = lvl;
    }
    public int getCuteness() {
        return this.cutenessLvl;
    }
    public String getCatType() {
        return this.catType;
    }
    public void setCatType(String type) {
        this.catType = type;
    }
    public void setCutenessLvl(int lvl) {
        this.cutenessLvl = lvl;
    }
    public static void main(String[] args) {
        Cat catA = new Cat();
        System.out.println("Cat A Type: " + catA.getCatType()); 
        System.out.println("Cat A Cuteness: " + catA.getCuteness()); 
       
        Cat catB = new Cat("sting", 21);
        System.out.println("Cat B Type: " + catB.getCatType()); 
        System.out.println("Cat B Cuteness: " + catB.getCuteness());
    }
}
